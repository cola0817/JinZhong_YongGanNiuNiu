public class VarVariable {
    public static void main(String[] args) {
        var int_01 = 10;
        var double_01 = 10.0;
        var char_01 = 'A';
        var char_02 = (char) 65;
        var String_01 = "Cola";

        System.out.println(int_01);
        System.out.println(double_01);
        System.out.println(char_01);
        System.out.println(char_02);
        System.out.println(String_01);
    }
}
